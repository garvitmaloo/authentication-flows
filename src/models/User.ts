import mongoose from 'mongoose';

import { IUser } from 'src/types';

export const userSchema = new mongoose.Schema({
  username: {
    type: String,
    required: true,
    unique: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
});

const User = mongoose.model('User', userSchema);

export type UserDocument = mongoose.Document & IUser;
export default User;
